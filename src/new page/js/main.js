function myFunction() {
  var x = document.getElementById('circle');
  if (x.style.backgroundColor === 'rgba(0, 0, 0, 0)') {
    x.style.backgroundColor = 'rgba(255, 255, 255, 1)';
  } else {
    x.style.backgroundColor = 'rgba(0, 0, 0, 0)';
  }
};

